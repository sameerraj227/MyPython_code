import numpy as np
array=np.array([[1, 2, 3], [4, 5, 5], [5, 6, 6], [6, 7, 8], [9, 10, 9]])
print(np.sum(array))  # Sum of all elements 
print(np.mean(array))  # Mean of all elements
print(np.max(array))  # Maximum element
print(np.min(array))  # Minimum element 
print(np.std(array))  # Standard deviation
print(np.var(array))  # Variance of elements
print(np.median(array))  # Median of elements
print(np.prod(array))  # Product of all elements
print(np.cumsum(array))  # Cumulative sum of elements
print(np.cumprod(array))  # Cumulative product of elements
print(np.transpose(array))  # Transpose of the array
print(np.shape(array))  # Shape of the array